# ── Helpers ───────────────────────────────────────────────────────────────────

def is_gurmukhi_char(ch):
    return '\u0A00' <= ch <= '\u0A7F'


def add_mixed_para(doc, text, base_size=11, indent=False, center=False):
    """
    Render a paragraph with mixed Gurmukhi + Russian text.
    - Gurmukhi script → dark red
    - ALL-CAPS words (3+ chars) → bold black
    - rest → black
    """
    p = doc.add_paragraph()
    if center:
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    if indent:
        p.paragraph_format.left_indent = Cm(1.2)

    # Split text into segments: gurmukhi / non-gurmukhi
    segments = []
    buf = []
    cur_gurmukhi = None
    for ch in text:
        g = is_gurmukhi_char(ch)
        if cur_gurmukhi is None:
            cur_gurmukhi = g
        if g != cur_gurmukhi:
            segments.append((cur_gurmukhi, ''.join(buf)))
            buf = []
            cur_gurmukhi = g
        buf.append(ch)
    if buf:
        segments.append((cur_gurmukhi, ''.join(buf)))

    for is_gurm, seg in segments:
        if not seg:
            continue
        if is_gurm:
            run = p.add_run(seg)
            run.font.name = 'Arial'
            run.font.size = Pt(base_size)
            run.font.color.rgb = C_GURMUKHI
            run.font.bold = False
        else:
            # Further split on ALL-CAPS words
            import re
            parts = re.split(r'(\b[А-ЯA-Z]{3,}\b)', seg)
            for part in parts:
                if not part:
                    continue
                run = p.add_run(part)
                run.font.name = 'Arial'
                run.font.size = Pt(base_size)
                run.font.color.rgb = C_BLACK
                run.font.bold = bool(re.match(r'^[А-ЯA-Z]{3,}$', part))
    return p


def add_image(doc, key):
    path = IMGS.get(key)
    if not path or not os.path.exists(path):
        print(f'  Image not found: {key}')
        return
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run()
    try:
        run.add_picture(path, width=Inches(5.5))
    except Exception as e:
        print(f'  Image insert failed ({key}): {e}')


