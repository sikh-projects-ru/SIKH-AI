req = urllib.request.Request(URL, headers={'User-Agent': 'Mozilla/5.0'})
with urllib.request.urlopen(req) as r:
    raw = r.read().decode('utf-8', errors='replace')

# locate content div
parts = list(re.finditer(r'<div[^>]+class="td-post-content"', raw))
start = parts[0].start()
end_m = re.search(r'<div[^>]+class="td-post-source-tags"', raw[start:])
content = raw[start: start + end_m.start()] if end_m else raw[start: start+60000]

block_re = re.compile(r'<(h[1-6]|p|hr)\b([^>]*)>(.*?)</\1>',
                      re.IGNORECASE | re.DOTALL)
raw_nodes = list(block_re.finditer(content))
print(f'Parsed {len(raw_nodes)} nodes')

# ── build docx ────────────────────────────────────────────────────────────────

HEADING_STYLES = {
    4: dict(size=13, bold=True,  color=(0x55, 0x00, 0x00)),
    5: dict(size=12, bold=True,  color=(0x20, 0x20, 0x60)),
    6: dict(size=11, bold=True,  color=(0x20, 0x20, 0x60)),
}

doc = Document()

# margins
for sec in doc.sections:
    sec.top_margin    = Cm(2)
    sec.bottom_margin = Cm(2)
    sec.left_margin   = Cm(2.5)
    sec.right_margin  = Cm(2.5)


def add_para(text, size=11, bold=False, italic=False,
             color=(0, 0, 0), indent=False, center=False):
    p = doc.add_paragraph()
    if center:
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    if indent:
        p.paragraph_format.left_indent = Cm(1.2)
    run = p.add_run(text)
    run.font.name  = 'Arial'
    run.font.size  = Pt(size)
    run.font.bold  = bold
    run.font.italic = italic
    run.font.color.rgb = RGBColor(*color)
    return p


def add_image(path):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run()
    run.add_picture(path, width=Inches(5.5))


images_inserted = 0

for i, node in enumerate(raw_nodes):
    if i in SKIP:
        continue

    tag   = node.group(1).lower()
    attrs = node.group(2)
    inner = node.group(3)

    # heading level
    hlevel = int(tag[1]) if tag.startswith('h') else 0

    # detect indent paragraph (transliteration: starts with letter, no <strong>)
    plain_text = re.sub(r'<[^>]+>', '', inner).strip()
    plain_text = html_module.unescape(plain_text)

    is_indent = (
        hlevel == 0 and
        '<strong' not in inner.lower() and
        re.match(r'^[A-Za-z]', plain_text) and
        i > 0
    )

    # ── Gurmukhi H4 or transliteration — keep as-is ──────────────────────────
    if hlevel == 4 or is_indent:
        style = HEADING_STYLES.get(hlevel, {})
        sub_nodes = split_by_imgs(inner)
        for sn in sub_nodes:
            if sn['type'] == 'img':
                add_image(sn['path'])
                images_inserted += 1
            else:
                t = re.sub(r'<[^>]+>', '', sn['html']).strip()
                t = html_module.unescape(t)
                if not t:
                    continue
                if hlevel == 4:
                    add_para(t,
                             size=style['size'], bold=style['bold'],
                             color=style['color'])
                else:
                    add_para(t, size=10, italic=True,
                             color=(0x40, 0x40, 0x40), indent=True)
        continue

    # ── look up RU translation ────────────────────────────────────────────────
    display = RU.get(i, None)

    sub_nodes = split_by_imgs(inner)
    for sn in sub_nodes:
        if sn['type'] == 'img':
            add_image(sn['path'])
            images_inserted += 1
        else:
            if display is None:
                continue
            t = display if display else html_module.unescape(
                    re.sub(r'<[^>]+>', '', sn['html']).strip())
            if not t or not t.strip():
                continue
            if hlevel in HEADING_STYLES:
                style = HEADING_STYLES[hlevel]
                add_para(t, size=style['size'], bold=style['bold'],
                         color=style['color'])
            else:
                add_para(t)

doc.save(OUTPUT)
print(f'\nSaved: {OUTPUT}')
print(f'Images inserted: {images_inserted}')
