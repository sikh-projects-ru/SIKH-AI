# ── Построение DOCX ───────────────────────────────────────────────────────────
def add_separator(doc):
    """Тонкая горизонтальная линия между строфами."""
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    pPr = p._p.get_or_add_pPr()
    pBdr = OxmlElement('w:pBdr')
    bottom = OxmlElement('w:bottom')
    bottom.set(qn('w:val'), 'single')
    bottom.set(qn('w:sz'), '4')
    bottom.set(qn('w:space'), '1')
    bottom.set(qn('w:color'), 'CCCCCC')
    pBdr.append(bottom)
    pPr.append(pBdr)
    run = p.add_run(' ')
    run.font.size = Pt(4)
    return p


def add_text(doc, text, color, font_name, size, bold=False, italic=False, center=True, space_after=0):
    p = doc.add_paragraph()
    if center:
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    pf = p.paragraph_format
    pf.space_before = Pt(0)
    pf.space_after = Pt(space_after)
    run = p.add_run(text)
    run.font.name = font_name
    # Force font for Gurmukhi script via XML (East Asian / Complex script)
    rPr = run._r.get_or_add_rPr()
    rFonts = OxmlElement('w:rFonts')
    rFonts.set(qn('w:ascii'), font_name)
    rFonts.set(qn('w:hAnsi'), font_name)
    rFonts.set(qn('w:cs'), font_name)   # complex script
    rPr.insert(0, rFonts)
    run.font.color.rgb = color
    run.font.bold = bold
    run.font.italic = italic
    run.font.size = Pt(size)
    return p


def build_ang(doc, lines_data):
    last_section = None

    for i, (section, punjabi, translit, russian) in enumerate(lines_data):
        # Метка раздела (Паури 1, Шалок и т.д.)
        if section and section != last_section:
            sp = doc.add_paragraph()
            sp.alignment = WD_ALIGN_PARAGRAPH.CENTER
            sp.paragraph_format.space_before = Pt(14)
            sp.paragraph_format.space_after = Pt(2)
            run = sp.add_run(f'— {section} —')
            run.font.name = F_HEADING
            run.font.color.rgb = C_PAURI
            run.font.size = Pt(9)
            run.font.bold = True
            last_section = section

        # Punjabi строка (Gurmukhi)
        add_text(doc, punjabi, C_GURBANI, F_GURMUKHI, size=16, bold=True, space_after=1)

        # Транслитерация
        add_text(doc, translit, C_TRANSLIT, F_LATIN, size=10.5, italic=True, space_after=1)

        # Русский перевод
        add_text(doc, russian, C_RUSSIAN, F_LATIN, size=11, space_after=6)

        # Разделитель между строфами (перед следующим разделом или каждые 2 строки)
        next_has_section = (i + 1 < len(lines_data)) and lines_data[i + 1][0] is not None
        is_last = i == len(lines_data) - 1
        if next_has_section and not is_last:
            doc.add_paragraph().paragraph_format.space_after = Pt(4)


