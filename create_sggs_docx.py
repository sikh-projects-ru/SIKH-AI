_source_doc = None

def _get_source():
    global _source_doc
    if _source_doc is None:
        _source_doc = Document(SOURCE_DOCX)
    return _source_doc


def fetch_page(para_start, para_end):
    """Return (cls, text) blocks from source docx paragraph range."""
    doc = _get_source()
    blocks = []
    for para in doc.paragraphs[para_start:para_end]:
        text = para.text.strip()
        if not text:
            continue
        cls = STYLE_TO_CLS.get(para.style.name, 'blackuni')
        blocks.append((cls, text))
    return blocks


