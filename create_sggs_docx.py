import re as _re
import unicodedata as _ud

def _norm(s):
    """Normalize whitespace, quotes, and Unicode form for matching."""
    s = _ud.normalize('NFC', s)
    s = _re.sub(r'\s+', ' ', s)
    # Normalize curly/typographic quotes to straight ASCII
    s = s.replace('\u2018', "'").replace('\u2019', "'")  # '' → '
    s = s.replace('\u201c', '"').replace('\u201d', '"')  # "" → "
    return s

def get_translation(text):
    t = _norm(text)
    for key, val in RU_TRANSLATIONS:
        if _norm(key) in t:
            return val
    return None


# ── 5. Build DOCX ──────────────────────────────────────────────────────────
def add_para(doc, text, rgb, bold=False, italic=False, size=12, center=False):
    p = doc.add_paragraph()
    if center:
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    run.font.color.rgb = RGBColor(*rgb)
    run.font.bold = bold
    run.font.italic = italic
    run.font.size = Pt(size)
    return p


def build_section(doc, title, para_start, para_end):
    blocks = fetch_page(para_start, para_end)

    # Section header
    add_para(doc, f'-- {title} --',
             (0x40, 0x40, 0x40), bold=True, size=13, center=True)
    doc.add_paragraph('')

    for cls, text in blocks:
        color = LABEL_COLORS.get(cls, (0, 0, 0))
        is_bani = cls in ('bani', 'banihead', 'banicenter')
        is_center = cls in ('banihead', 'banicenter')

        if is_bani:
            # Sacred text: Gurmukhi + transliteration + Russian
            add_para(doc, text, color, bold=True, size=14, center=is_center)
            trl = get_translit(text)
            if trl:
                tp = doc.add_paragraph()
                if is_center:
                    tp.alignment = WD_ALIGN_PARAGRAPH.CENTER
                run = tp.add_run(trl)
                run.font.color.rgb = RGBColor(*TRANSLIT_COLOR)
                run.font.italic = True
                run.font.size = Pt(11)
            ru = get_translation(text)
            if ru:
                rp = doc.add_paragraph()
                run = rp.add_run(ru)
                run.font.color.rgb = RGBColor(*color)
                run.font.size = Pt(11)
        else:
            # Commentary: Russian translation only (no Punjabi source)
            ru = get_translation(text)
            if ru:
                rp = doc.add_paragraph()
                run = rp.add_run(ru)
                run.font.color.rgb = RGBColor(*color)
                run.font.size = Pt(11)
            else:
                # Fallback: show original if no translation yet
                add_para(doc, text, color, size=12)

    doc.add_page_break()
    return len(blocks)


def build_page(doc, page_num):
    """Legacy wrapper kept for compatibility."""
    return build_section(doc, f'Страница {page_num}', CONTENT_START, CONTENT_START + 200)


