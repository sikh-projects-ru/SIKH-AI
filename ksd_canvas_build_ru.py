def para(doc, text, bold=False, italic=False, size=11, color=None,
         center=False, indent=False):
    p = doc.add_paragraph()
    if center: p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    if indent: p.paragraph_format.left_indent = Inches(0.4)
    run = p.add_run(text)
