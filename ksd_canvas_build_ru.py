def strip_tags(s):
    s = re.sub(r'<[^>]+>', '', s)
    return html_module.unescape(s).strip()

def extract_links(s):
    return re.findall(r'<a[^>]+href="([^"]*)"[^>]*>(.*?)</a>', s, re.DOTALL)

def best_img_src(tag_html):
    m = re.search(r'\bsrc="([^"]+)"', tag_html)
    if not m: return None
    src = m.group(1)
    if re.search(r'-\d+x\d+\.(jpg|png)', src): return None
    return src

def emit_img_node(tag_html):
    src = best_img_src(tag_html)
    if not src or 'uploads/2021' not in src: return None
    alt_m = re.search(r'alt="([^"]*)"', tag_html)
    alt = html_module.unescape(alt_m.group(1)) if alt_m else ''
    return ('img', src, alt)

def split_by_imgs(inner_html):
    parts = []
    pos = 0
    for m in re.finditer(r'<img\b[^>]*/?>',  inner_html, re.IGNORECASE | re.DOTALL):
        before = inner_html[pos:m.start()]
        if before.strip(): parts.append(('text', before))
        node = emit_img_node(m.group(0))
        if node: parts.append(node)
        pos = m.end()
    tail = inner_html[pos:]
    if tail.strip(): parts.append(('text', tail))
    return parts

# Build node list with text index
nodes = []
text_idx = 0
block_re = re.compile(r'<(h[1-6]|p|hr)\b([^>]*)>(.*?)</\1>', re.IGNORECASE | re.DOTALL)

for m in block_re.finditer(article_html):
    tag   = m.group(1).lower()
    attrs = m.group(2)
    inner = m.group(3)

    if tag == 'hr':
        nodes.append(('hr',))
        continue

    for part in split_by_imgs(inner):
        if part[0] == 'img':
            nodes.append(part)
        else:
            text_en = strip_tags(part[1])
            if not text_en or text_en in ('\xa0',): continue
            links = extract_links(part[1])
            indent = 'padding-left' in attrs
            level = int(tag[1]) if tag[0] == 'h' else 0
            nodes.append(('text', text_idx, level, links, indent))
            text_idx += 1

print(f"Nodes: {len(nodes)}, text nodes: {text_idx}, images: {sum(1 for n in nodes if n[0]=='img')}")

# ── Download images ───────────────────────────────────────────────────────────
def download_img(url):
    fname = os.path.join(IMG_DIR, os.path.basename(url.split('?')[0]))
    if os.path.exists(fname): return fname
    try:
        req = urllib.request.Request(url, headers={'User-Agent': 'Mozilla/5.0'})
        with urllib.request.urlopen(req, timeout=15) as r:
            data = r.read()
        with open(fname, 'wb') as f: f.write(data)
        print(f"  Downloaded: {os.path.basename(fname)}")
        return fname
    except Exception as e:
        print(f"  WARN: {url}: {e}")
        return None

